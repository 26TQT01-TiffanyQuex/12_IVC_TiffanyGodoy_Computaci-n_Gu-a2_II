    private void jbtnBorrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBorrar1ActionPerformed
        String texto = jtxtCantidad.getText(); //Aquí va a guardar lo que esté en el TextField
        if (texto.length() > 1) { //verifica si hay más de un caracter
        texto = texto.substring(0, texto.length()-1); //substring va a tomar una parte del texto
        jtxtCantidad.setText(texto);                  //y texto.length()-1 va a devolver la cantidad de digitos escrita menos 1.
        }else {
            jtxtCantidad.setText("0");
        }
    }//GEN-LAST:event_jbtnBorrar1ActionPerformed

