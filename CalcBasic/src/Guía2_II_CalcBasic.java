//Se crea un nuevo método fuera de cualquier botón para que la operación matemática no dependa de ninguno
//Y en lugar de repetir una operación puede solo llamar al método y realizar otra aopración seguida sin presionar igual.
    private void calcular() {
        if (!entrada.isEmpty()) { //"!.isEmpty" afirma que la entrada está vacía "SI NO ESTÁ VACÍA"
        numero2 = Double.parseDouble(entrada); //en el numero2 guardará el valor que se haya ingresado como double.
    }
        if (operador == null || operador.isEmpty()) { //Esta línea es necesaria porque indica que el operador aún no tiene valor, y si tiene está vacío, por eso las dos condiciones.
        resultado = numero2; //aquí se va a guardar el dato ingresado para que si no hay operador se muestre el último valor.
    //Aquí empieza el código para cada operación
    } else if (operador.equals("x")) { 
        resultado = numero1 * numero2;
    } else if (operador.equals("÷")) { //Se usa .equals para comparar el texto de la variable con el string
        resultado = numero1 / numero2;
    } else if (operador.equals("+")) {
        resultado = numero1 + numero2;
    } else if (operador.equals("-")) {
        resultado = numero1 - numero2;
    } else if (operador.equals("%")) {
        resultado = numero1*numero2/100;
    }

    entrada = String.valueOf(resultado); //va a convertir el valor de entrada a texto y lo guarda en resultado.
    jtxtCantidad.setText(entrada);
    numero1= resultado;
    }
